        /*
         * Weapon and Armor Proficiency

An aurora soul is proficient with all simple weapons and with light armor, but not with shields. When wearing medium or heavy armor, using a shield, or carrying a medium or heavy load, an aurora soul loses the benefits of her mystic combat and defensive aura class features.

This ability replaces the mystic’s normal weapon and armor proficiencies.
Maneuvers

The disciplines available to an aurora soul are Broken Blade, Elemental Flux, Riven Hourglass, Steel Serpent, Thrashing Dragon, and Veiled Moon. An aurora soul gains Heal as a class skill. She otherwise learns, readies, and initiates maneuvers as a standard mystic.

This ability alters maneuvers.
Mystic Combat (Su)

An aurora soul learns how to passively channel animus through her body, increasing the strength of her bare-handed blows. An aurora soul’s unarmed strike is treated as both a manufactured weapon and a natural weapon for the purpose of spells and effects that enhance or improve either manufactured weapons or natural weapons. In addition, she gains the following benefits:

    At 1st level, an aurora soul gains Improved Unarmed Strike as a bonus feat.
    At 3rd level, an aurora soul gains Greater Unarmed Strike as a bonus feat, even if she does not meet the prerequisites.
    Starting at 7th level, an aurora soul adds her aurora soul initiation modifier as a bonus on damage rolls with unarmed strikes, and her unarmed strikes are treated as magic weapons for the purpose of overcoming damage reduction.
    Starting at 12th level, an aurora soul adds her aurora soul initiation modifier as an insight bonus to combat maneuver checks and to her combat maneuver defense when fighting unarmed.
    Starting at 17th level, an aurora soul adds her aurora soul initiation modifier as an insight bonus on critical confirmation rolls made with unarmed strikes.

This ability replaces bonus feats.
Defensive Aura (Su)

Starting at 2nd level, an aurora soul’s animus creates a shimmering shield around her body, warding off blows and pulsing with scintillating colors. Whenever the aurora soul has at least one point of animus in her animus pool, she adds her aurora soul initiation modifier to her AC. This bonus to AC applies even against touch attacks or when the aurora soul is flat-footed. Note that the aurora soul’s defensive aura comes into being at the start of her first turn in a combat (at the same time as her animus pool); she does not gain this against attacks made before she acts. The defensive aura dissipates after combat, at the same time the aurora soul’s animus pool does.

This ability replaces mystic artifice. 
