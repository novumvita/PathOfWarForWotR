    class Stalker_BrutalSlayer
    {
        /*
         * Where other stalkers use grace and finesse, the brutal slayer relies on strength and fury. Wielding heavy, two handed weapons with blinding speed, the brutal slayer is a terrifying predator of the night. 
         */

        /*
         * Maneuvers

A brutal slayer loses access to the Thrashing Dragon discipline and adds Black Seraph and Primal Fury to his list of available disciplines. He gains Survival as a class skill, and otherwise learns, readies, and initiates maneuvers as a standard stalker.

This ability alters maneuvers.
Maneuvers Readied

Instead of recovering maneuvers like a standard stalker a brutal slayer uses his ki to reinforce his body as a full-round action. When he does so, he recovers a number of expended maneuvers equal to his brutal slayer initiation modifier (minimum 2) and gains temporary hit points equal to twice his initiator level and damage reduction/– equal to his brutal slayer initiation modifier (minimum 0). These temporary hit points and damage reduction last for one round, and stack with any other sources of temporary hit points or damage reduction he may have. In addition, the next strike he initiates this encounter deals additional bleed damage equal to his brutal stalker initiation modifier. The target continues to bleed until the bleeding is stopped via magical healing or a successful DC 15 Heal check. Alternately, the brutal slayer can take a moment to realign himself and recover a single maneuver as a standard action.

This ability alters maneuvers readied.
Brutal Strikes (Ex)

Starting at 1st level, whenever a brutal slayer confirms a critical hit against a creature, he gains a +4 profane bonus to Strength for a number of rounds equal to his brutal slayer initiation modifier. This bonus increases to +6 at 8th level and to +8 at 16th level.

This ability replaces deadly strikes.
Stalker Arts (Ex or Su)

Whenever a brutal slayer gains a new stalker art, he may choose from the following list in addition to the standard stalker’s stalker arts. This ability alters stalker arts, but does not cause the brutal slayer archetype to be incompatible with other archetypes that alter the stalker arts class feature.

Brutal Ambush (Ex): Whenever the brutal slayer attacks a flat-footed opponent, he increases his Strength while making that attack as if he had activated his brutal strikes ability. This benefit ends after his attack resolves.

Brutal Assault (Ex): As a swift action, the brutal slayer can spend a point of ki and target a single creature he can see. He gains the benefit of his brutal strikes ability when making attacks against that creature for a number of rounds equal to his initiation modifier.

Furious Strikes (Ex): The Strength bonus granted by the brutal slayer’s brutal strikes ability increases by +2. In addition, he treats his class level as his base attack bonus for the purposes of qualifying for critical feats. If the brutal slayer possesses fighter levels, he also adds his brutal slayer level to his fighter level for the purposes of qualifying for critical feats.

Cruel Strike (Ex): When the brutal slayer hits with a melee attack while under the effect of his brutal strikes ability, he can reduce the damage he deals by 1/4 (rounded down) to sicken the target for a number of rounds equal to his brutal slayer initiation modifier.
Armor of Scars (Ex)

The brutal slayer takes as well as he gives, becoming protected by the scars of wounds he has already survived. At 1st level, a brutal slayer gains a +1 bonus to his natural armor bonus to AC. (this stacks with his existing natural armor bonus, if any) At 4th level and every four levels thereafter, this bonus increases by +1. In addition, the brutal slayer’s instincts enable him to better dodge incoming blows; whenever he spends a full-round action to recover maneuvers, he gains a dodge bonus to AC equal to his brutal slayer initiation modifier.

This ability replaces dodge bonus.
Muscle Memory (Ex)

The muscles of a brutal slayer are honed to deadly efficiency, just like his blades. Starting at 2nd level, a brutal slayer may add 1/2 his Strength modifier to his AC and to Reflex saves instead of his Dexterity modifier. At 6th level, the brutal slayer may add his full Strength modifier instead of his Dexterity modifier, rather than 1/2. The brutal slayer’s Strength bonus to his AC is still limited by his armor’s maximum Dexterity bonus. 
         */
    }
}
