namespace PathOfWarForWotR.NewContent.MartialClasses.Archetypes
{
    class Stalker_Vigilante
    {
        /*
         * Vigilantes are warriors who use fear and darkness to their advantage. They rely on keen insight and inspiration to aid them where others would seek to use magic or the supernatural. 
         */
        /*
         * Class Skills

A vigilante gains Knowledge (dungeoneering), Knowledge (history), Knowledge (geography), Knowledge (nobility), and Sleight of Hand as class skills.
Maneuvers

A vigilante loses access to the Solar Wind and Veiled Moon disciplines and adds Primal Fury, Scarlet Throne, and Tempest Gale to his list of available disciplines. His initiation modifier is Intelligence, and any stalker class features that previously used his Wisdom modifier now use his Intelligence modifier. He otherwise learns, readies, and initiates maneuvers as a standard stalker.

This ability alters maneuvers.
Maneuvers Readied

Instead of recovering maneuvers like a standard stalker, a vigilante must open his mind to the flow of battle and the inherent patterns therein as a full-round action. When he does so, he recovers a number of expended maneuvers equal to his vigilante initiation modifier (minimum 2), gains a +4 insight bonus to his AC for one round and can move up to to his speed during the action, provoking attacks of opportunity as normal. In addition, the next attack he makes this encounter adds his sneak attack damage if it hits, regardless of whether or not the target his flanked or flat-footed. Alternately, the vigilante can center his awareness and recover a single maneuver as a standard action.

This ability alters maneuvers readied.
Inspiration (Ex)

A vigilante relies on his insight to aid him in his mission, using his cunning to augment skill checks and ability checks. The vigilante has an inspiration pool equal to 1/2 his class level + his vigilante initiation modifier (minimum 1). A vigilante’s inspiration pool refreshes each day, typically after he gets a restful night’s sleep. As a free action, he can expend one use of inspiration from his pool to add 1d6 to the result of that check, including any on which he takes 10 or 20. This choice is made after the check is rolled and before the results are revealed. A vigilante can only use inspiration once per check or roll. The vigilante can use inspiration on any Knowledge, Perception, or Sense Motive skill checks without expending a use of inspiration, provided he’s trained in the skill.

Inspiration can also be used on attack rolls and saving throws, although it costs two points of inspiration rather than one. Using inspiration while making a saving throw is an immediate action, rather than a free action.

This ability replaces ki pool.
Trapfinding (Ex)

A vigilante adds 1/2 his level as a bonus on Perception checks to locate traps and to Disable Device checks (minimum 1). A vigilante can use Disable Device to disarm magical traps.
Sneak Attack (Ex)

If a vigilante can catch an opponent when he is unable to defend himself effectively from the vigilante’s attack, he can strike a vital spot for extra damage.

The vigilante’s attack deals extra damage any time his target would be denied a Dexterity bonus to AC (whether the target actually has a Dexterity bonus or not), or when the vigilante flanks his target. This extra damage is 1d6 at 1st level, and increases by 1d6 every three vigilante levels thereafter (2d6 at 4th level, 3d6 at 7th level, and so on). Ranged attacks can count as sneak attacks only if the target is within 30 feet. This additional damage is precision damage and is not multiplied on a critical hit.

With a weapon that deals nonlethal damage (such as a sap, unarmed strike, or whip), a vigilante can make a sneak attack that deals nonlethal damage instead of lethal damage. He cannot use a weapon that deals lethal damage to deal nonlethal damage in a sneak attack—not even with the usual –4 penalty.

The vigilante must be able to see the target well enough to pick out a vital spot and must be able to reach such a spot. A vigilante cannot sneak attack while striking a creature with total concealment.

This ability replaces deadly strike.
Stalker Arts (Ex or Su)

Instead of selecting a stalker art at the appropriate level, a vigilante can select an investigator talent instead, treating his stalker level as his effective investigator level. He must meet the prerequisites for this investigator talent as normal. The vigilante cannot select investigator talents which augment alchemy, nor can he select stalker arts which require a ki pool to function. Any investigator talent which augments studied strike instead affects the vigilante’s sneak attacks.

This ability alters stalker arts, but does not cause the vigilante archetype to be incompatible with other archetypes that alter the stalker arts class feature.
Inspired Vengeance (Ex)

At 20th level, a vigilante has mastered inspiration to such an extent that he instinctively places his attacks in near-perfect paths. The vigilante can use inspiration on any attack roll he makes without spending any points from his pool. In addition, the vigilante can spend one point of inspiration once per round as a free action to to add 1d6 the save DC of a maneuver he initiates.

This ability replaces retributive ki. 
         */
    }
}
